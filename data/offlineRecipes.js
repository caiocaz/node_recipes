const offlineRecipes = Object.values(offlineRecipesByCategory).flat()

// Ensure every offline recipe has a link (fallback to exemplo.com.br)
offlineRecipes.forEach(r => {
  if (!r.link) r.link = 'https://exemplo.com.br'
})

export function getOfflineRecipes(type = '') {
  const normalizedType = String(type || '').trim().toLowerCase()
  if (!normalizedType) {
    return offlineRecipes
  }

  const matches = offlineRecipes.filter(recipe => {
    const text = `${recipe.title} ${recipe.category} ${recipe.preparation} ${recipe.ingredients.map(i => i.ingredient).join(' ')}`.toLowerCase()
    return text.includes(normalizedType)
  })

  if (matches.length >= 3) {
    return matches
  }

  const categoryMatch = Object.keys(offlineRecipesByCategory).find(category => category.toLowerCase().includes(normalizedType))
  if (categoryMatch) {
    return offlineRecipesByCategory[categoryMatch]
  }

  if (matches.length > 0) {
    return matches
  }

  const primary = offlineRecipesByCategory['Pratos Principais'] || []
  return primary.length ? primary : offlineRecipes.slice(0, 3)
}

export function getFeaturedOfflineRecipes(count = 3) {
  return offlineRecipes.slice(0, count)
}
